#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - op
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Entry point
 * @a: variable
 * @b: variable
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Entry point
 * @a: variable
 * @b: variable
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Entry point
 * @a: variable
 * @b: variable
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Entry point
 * @a: variable
 * @b: variable
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
