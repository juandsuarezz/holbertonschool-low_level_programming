#include <stdio.h>
/**
 *main - Entry point
 *
 *Description: This program will take a random number and display whether it is
 *positive, negative, or zero.
 *Return: Returns 0 upon successful run.
 */
int main(void)
{
char c;
for (c = 'a' ; c <= 'z' ; c++)
putchar (c);
char d;
for (d = 'A' ; d <= 'Z' ; d++)
putchar (d);
putchar('\n');
return (0);
}
